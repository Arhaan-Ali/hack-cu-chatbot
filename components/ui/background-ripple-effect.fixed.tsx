"use client";
import React, { useMemo, useRef, useState } from "react";
import { cn } from "@/lib/utils";
import type {
  DivGridProps,
  CellStyle,
} from "@/types/background-ripple-effect.interface";

const BackgroundRippleEffect = ({
  rows = 8,
  cols = 27,
  cellSize = 56,
}: {
  rows?: number;
  cols?: number;
  cellSize?: number;
}) => {
  const [clickedCell, setClickedCell] = useState<{
    row: number;
    col: number;
  } | null>(null);
  const [rippleKey, setRippleKey] = useState(0);
  const ref = useRef<HTMLDivElement | null>(null);

  const DivGrid = ({
    className,
    rows,
    cols,
    cellSize,
    borderColor,
    fillColor,
    clickedCell,
    onCellClick,
    interactive,
  }: DivGridProps & {
    clickedCell?: { row: number; col: number } | null;
    onCellClick?: (row: number, col: number) => void;
    interactive?: boolean;
  }) => {
    const cells = useMemo(
      () => Array.from({ length: (rows ?? 0) * (cols ?? 0) }, (_, i) => i),
      [rows, cols],
    );
    const gridStyle: React.CSSProperties = {
      display: "grid",
      gridTemplateColumns: `repeat(${cols}, ${cellSize}px)`,
      gridTemplateRows: `repeat(${rows}, ${cellSize}px)`,
      width: (cols ?? 0) * (cellSize ?? 0),
      height: (rows ?? 0) * (cellSize ?? 0),
      marginInline: "auto",
    };

    return (
      <div className={cn("relative z-3", className)} style={gridStyle}>
        {cells.map((idx) => {
          const rowIdx = Math.floor(idx / (cols ?? 1));
          const colIdx = idx % (cols ?? 1);
          const distance = clickedCell
            ? Math.hypot(clickedCell.row - rowIdx, clickedCell.col - colIdx)
            : 0;
          const delay = clickedCell ? Math.max(0, distance * 55) : 0; // ms
          const duration = 200 + distance * 80; // ms

          const style: CellStyle = clickedCell
            ? {
                "--delay": `${delay}ms`,
                "--duration": `${duration}ms`,
              }
            : {};

          return (
            <div
              key={idx}
              className={cn(
                "cell relative border-[0.5px] opacity-40 transition-opacity duration-150 will-change-transform hover:opacity-80 dark:shadow-[0px_0px_40px_1px_var(--cell-shadow-color)_inset]",
                clickedCell && "animate-cell-ripple fill-mode-[none]",
                !interactive && "pointer-events-none",
              )}
              style={{
                backgroundColor: fillColor,
                borderColor: borderColor,
                ...style,
              }}
              onClick={
                interactive ? () => onCellClick?.(rowIdx, colIdx) : undefined
              }
            />
          );
        })}
      </div>
    );
  };

  return (
    <div
      ref={ref}
      className={cn(
        "absolute inset-0 h-full w-full",
        "[--cell-border-color:var(--color-neutral-300)] [--cell-fill-color:var(--color-neutral-100)] [--cell-shadow-color:var(--color-neutral-500)]",
        "dark:[--cell-border-color:var(--color-neutral-700)] dark:[--cell-fill-color:var(--color-neutral-900)] dark:[--cell-shadow-color:var(--color-neutral-800)]",
      )}
    >
      <div className="relative h-auto w-auto overflow-hidden">
        <div className="pointer-events-none absolute inset-0 z-2 h-full w-full overflow-hidden" />
        <DivGrid
          key={`base-${rippleKey}`}
          className="mask-radial-from-20% mask-radial-at-top opacity-600"
          rows={rows}
          cols={cols}
          cellSize={cellSize}
          borderColor="var(--cell-border-color)"
          fillColor="var(--cell-fill-color)"
          clickedCell={clickedCell}
          onCellClick={(row, col) => {
            setClickedCell({ row, col });
            setRippleKey((k) => k + 1);
          }}
          interactive
        />
      </div>
    </div>
  );
};

export default BackgroundRippleEffect;
